num1 = 10
num2 = 4

print("Num1 & Num2 =", num1 & num2)  # Bitwise AND
print("\nNum1 | Num2 =", num1 | num2)  # Bitwise OR
print("\n~Num1 =", ~num1)  # Bitwise NOT
print("\nNum1 ^ Num2 =", num1 ^ num2)  # Bitwise XOR

num1 = 10
num2 = 4

print("\nNum1 >> 2 =", num1 >> 2)  # Right Shift
print("\nNum2 >> 2 =", num2 >> 2)  # Right Shift

num1 = 10
num2 = 4

print("\nNum1 << 2 =", num1 << 2)  # Left Shift
print("\nNum2 << 2 =", num2 << 2)  # Left Shift